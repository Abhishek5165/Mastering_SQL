-- INSERT - used to insert something in between ...

SELECT INSERT('HELLO WHATSUP',7,0,'RAJU ') AS NAME;

-- LEFT - It gives some part from the left, same as SUBSTR();

SELECT LEFT('ABCDEFGHIJKLMNOPQRSTUVWXYZ',10) AS NAME;

-- RIGHT - It gives some part from the right, same as SUBSTR();

SELECT RIGHT('ABCDEFGHIJKLMNOPQRSTUVWXYZ',10) AS NAME;

-- REPEAT - It is used to REPEAT the same word which i provide;

SELECT REPEAT('HaHaH ',10) AS NAME;

-- TRIM - used to remove the white spaces.

SELECT TRIM(" ABHISHEK      ") AS NAME;